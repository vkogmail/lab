function resolveTokenReference(value, coreTokens) {
  // Handle invalid values
  if (!value || value === 'NaN' || (typeof value === 'string' && value.includes('NaN'))) {
    return undefined;  // This will trigger the default value
  }
  
  if (typeof value !== 'string') return value;
  
  // If it's a direct value with units (e.g., "1.15" or "5deg" or "16px")
  if (!value.startsWith('{')) {
    return value;
  }
  
  const match = value.match(/^{(.+)}$/);
  if (!match) return value;
  
  const coreToken = coreTokens[match[1]]?.value;
  return coreToken || value;
}

function normalizeTokenValue(value, coreTokens) {
  console.log('Input value:', JSON.stringify(value, null, 2));

  // If it's a string reference
  if (typeof value === 'string') {
    return resolveTokenReference(value, coreTokens);
  }

  // If it has a value property that's a string (reference)
  if (value?.value && typeof value.value === 'string') {
    return resolveTokenReference(value.value, coreTokens);
  }

  // Get the actual value object to normalize
  const valueToNormalize = value?.value || value || {};

  // Always return a complete token structure
  const normalized = {
    type: valueToNormalize.type || 'bezier',
    duration: resolveTokenReference(valueToNormalize.duration, coreTokens) || '300ms',
    delay: resolveTokenReference(valueToNormalize.delay, coreTokens) || '0ms',
    easing: resolveTokenReference(valueToNormalize.easing, coreTokens) || 'cubic-bezier(0.4, 0, 0.2, 1)',
    opacityFrom: resolveTokenReference(valueToNormalize.opacityFrom, coreTokens) || '1',
    opacityTo: resolveTokenReference(valueToNormalize.opacityTo, coreTokens) || '1',
    scaleFrom: resolveTokenReference(valueToNormalize.scaleFrom, coreTokens) || '1',
    scaleTo: resolveTokenReference(valueToNormalize.scaleTo, coreTokens) || '1',
    rotateFrom: resolveTokenReference(valueToNormalize.rotateFrom, coreTokens) || '0deg',
    rotateTo: resolveTokenReference(valueToNormalize.rotateTo, coreTokens) || '0deg',
    translateFrom: resolveTokenReference(valueToNormalize.translateFrom, coreTokens) || '0px',
    translateTo: resolveTokenReference(valueToNormalize.translateTo, coreTokens) || '0px',
    stiffness: valueToNormalize.stiffness || 100,
    damping: valueToNormalize.damping || 10,
    mass: valueToNormalize.mass || 1,
    velocity: valueToNormalize.velocity || 0
  };

  console.log('Normalized token:', JSON.stringify(normalized, null, 2));
  return normalized;
}

function normalizeTokens(tokens, coreTokens) {
  return {
    patterns: tokens.patterns ? Object.entries(tokens.patterns).reduce((acc, [name, pattern]) => {
      acc[name] = {
        value: normalizeTokenValue(pattern.value, coreTokens),
        type: pattern.type || 'bezier',
        description: pattern.description || ''
      };
      return acc;
    }, {}) : {},
    components: tokens.components ? Object.entries(tokens.components).reduce((acc, [name, component]) => {
      acc[name] = {
        value: normalizeTokenValue(component.value, coreTokens),
        type: component.type || 'bezier',
        description: component.description || ''
      };
      return acc;
    }, {}) : {}
  };
}

