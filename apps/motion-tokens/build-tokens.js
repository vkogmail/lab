function processTokenValue(token, tokens, coreTokens) {
  // If token is a direct value
  if (typeof token === 'string') {
    if (token.startsWith('{')) {
      const reference = token.match(/{([^}]+)}/)[1];
      const [category, property] = reference.split('.');
      const resolvedToken = tokens[category]?.[property];
      
      // If the resolved token has extensions (like alpha)
      if (resolvedToken?.$extensions?.['studio.tokens']?.modify) {
        return processTokenValue(resolvedToken, tokens, coreTokens);
      }
      
      return resolvedToken?.value || token;
    }
    return token;
  }

  // If token has extensions (like shadow with alpha)
  if (token.$extensions?.['studio.tokens']?.modify) {
    const modify = token.$extensions['studio.tokens'].modify;
    if (modify.type === 'alpha') {
      const baseColor = typeof token.value === 'string' && token.value.startsWith('{')
        ? processTokenValue(token.value, tokens, coreTokens)
        : token.value;

      if (baseColor.startsWith('#')) {
        const r = parseInt(baseColor.slice(1, 3), 16);
        const g = parseInt(baseColor.slice(3, 5), 16);
        const b = parseInt(baseColor.slice(5, 7), 16);
        return `rgba(${r}, ${g}, ${b}, ${modify.value})`;
      }
    }
  }

  // If token has a value property
  if (token.value) {
    return processTokenValue(token.value, tokens, coreTokens);
  }

  return token;
}

function generateCssVariables(tokens, coreTokens) {
  const vars = [];
  
  // Process color tokens
  if (tokens.color) {
    Object.entries(tokens.color).forEach(([key, value]) => {
      if (typeof value === 'object' && value !== null) {
        if (Object.keys(value).length > 0 && !value.value) {
          Object.entries(value).forEach(([subKey, subValue]) => {
            const resolvedValue = processTokenValue(subValue, tokens, coreTokens);
            vars.push(`  --color-${key}-${subKey}: ${resolvedValue};`);
          });
        } else {
          const resolvedValue = processTokenValue(value, tokens, coreTokens);
          vars.push(`  --color-${key}: ${resolvedValue};`);
        }
      }
    });
  }

  // Handle shadow tokens
  if (tokens.shadow) {
    Object.entries(tokens.shadow).forEach(([key, value]) => {
      if (value.value && typeof value.value === 'object') {
        const shadowColor = processTokenValue(value.value.color, tokens, coreTokens);
        const shadowValue = `${value.value.x} ${value.value.y} ${value.value.blur} ${value.value.spread} ${shadowColor}`;
        vars.push(`  --shadow-${key}: ${shadowValue};`);
      }
    });
  }

  // Handle border tokens
  if (tokens.border?.radius) {
    Object.entries(tokens.border.radius).forEach(([key, value]) => {
      vars.push(`  --border-radius-${key}: ${value.value};`);
    });
  }

  return vars;
}

